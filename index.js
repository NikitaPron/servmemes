io.on("connection", (ws) => {
  console.log("new Client");
  ws.on("disconnect", () => {
    console.log("user disconnected");

    io.emit(
      COUNT_IN_ROOM,
      JSON.stringify({
        count: io.engine.clientsCount,
      })
    );
  });

  ws.emit(
    NEW_SITUATION,
    JSON.stringify({
      situation: currentSituation,
      cards: shuffle(imgs),
    })
  );

  ws.emit(CHAT_UPDATE, JSON.stringify({ chat }));
  ws.emit(NEW_MUSIC, JSON.stringify(music));

  io.emit(
    COUNT_IN_ROOM,
    JSON.stringify({
      count: io.engine.clientsCount,
    })
  );

  ws.on(NEW_SITUATION, (message) => {
    const data = JSON.parse(message);
    firstlySituations.push(data.situation);
  });

  ws.on(CHAT_UPDATE, (message) => {
    const data = JSON.parse(message);
    chat.push({
      userName: data.userName,
      chatText: data.chatText,
    });
    io.emit(CHAT_UPDATE, JSON.stringify({ chat }));
  });

  ws.on(SOMEONE_CHOOSE, (message) => {
    const data = JSON.parse(message);
    io.emit(
      SOMEONE_CHOOSE,
      JSON.stringify({
        userName: data.userName,
        refImg: data.refImg,
      })
    );
  });

  ws.on(CHANGE_MY_MEMES, (message) => {
    const data = JSON.parse(message);
    ws.emit(
      CHANGE_MY_MEMES,
      JSON.stringify({
        cards: shuffle(imgs),
      })
    );
  });

  ws.on(NEW_MUSIC, (message) => {
    const data = JSON.parse(message);
    if (data.musicURL.includes("https://www.youtube.com/")) {
      if (music.startSeconds > 60 || music.musicURL === "url") {
        music.startSeconds = 0;
        music.musicURL = data.musicURL;
        music.userName = data.userName;
        io.emit(NEW_MUSIC, JSON.stringify(music));
      } else {
        ws.emit(ONE_MINUTE_MUSIC_MISTAKE, JSON.stringify({}));
      }
    }
  });

  ws.on(ADMIN_INFO, (message) => {
    const data = JSON.parse(message);
    adminInfo(data);
  });
});

