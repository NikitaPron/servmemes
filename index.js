const TIMEOUT_SITUATION = "TIMEOUT_SITUATION";
const CLEAR_CHAT = "CLEAR_CHAT";
const CLEAR_TIMEOUT_MUSIC = "CLEAR_TIMEOUT_MUSIC";
const NEXT_SITUATION = "NEXT_SITUATION";

function adminInfo(data) {
  if (data.event === TIMEOUT_SITUATION) {
    timingSituation = parseInt(data.seconds) * 1000;
    clearInterval(intervalSituation);
    intervalSituation = setInterval(changeSituation, timingSituation);
  }

  if (data.event === CLEAR_CHAT) {
    chat.length = 0;
    chat.push({
      userName: "Admin",
      chatText: "Очистил чат",
    });
    io.emit(CHAT_UPDATE, JSON.stringify({ chat }));
  }

  if (data.event === CLEAR_TIMEOUT_MUSIC) {
    music.startSeconds = 61;
  }

  if (data.event === NEXT_SITUATION) {
    clearInterval(intervalSituation);
    intervalSituation = setInterval(changeSituation, timingSituation);
    changeSituation();
  }
}
