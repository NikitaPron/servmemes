let situationsToChange = situations.slice();

const chat = [];
const firstlySituations = [];

let currentSituation = situations[getRandomInt(0, situations.length)];

let intervalSituation = setInterval(changeSituation, timingSituation);

function changeSituation() {
  if (firstlySituations.length > 0) {
    currentSituation = firstlySituations.shift();
  } else {
    if (situationsToChange.length === 0) {
      situationsToChange = situations.slice();
    }
    const index = getRandomInt(0, situationsToChange.length - 1);
    currentSituation = situationsToChange[index];
    situationsToChange.splice(index, 1);
  }

  io.emit(
    NEW_SITUATION,
    JSON.stringify({
      situation: currentSituation,
      cards: shuffle(imgs),
      count: clients.size,
    })
  );
}

